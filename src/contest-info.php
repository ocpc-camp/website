<h3>Camp materials and silence period</h3>
<p>
  Based on your feedback, we will have a silence period until the end of
  November 2023, during which camp materials will not be released to the public.
  Therefore, we ask participants to not discuss the problems in public until that date.
</p>
<p>
  After the camp ends, we will give all participants access to camp materials.
</p>
<p>
  Most of our contests are fresh and developed for this camp. A small number of contests
  may be based on previous contests that have not been released to the general public.
  If you have seen some problems of a contest before, you can't participate on that day
  (and your participation fee will be reduced accordingly). We will privately contact participants
  who might be affected.
</p>

<h3>Rules</h3>
<p>
Contests will mostly follow
<a href="https://www.win.tue.nl/~wstomv/contests/acm-icpc-rules-summary.html">classical ICPC rules</a>,
in particular:
</p>
<ul>
  <li>Each contest typically lasts 5 hours.</li>
  <li>Ranking of teams is based on:
    <ul>
      <li>Number of problems solved;</li>
      <li>In case of a tie, total time for all accepted problems;</li>
      <li>Each rejected submission for an accepted problem adds 20 minutes penalty time;</li>
      <li>If there is still a tie, the time of acceptance for the last problem;</li>
    </ul>
  </li>
  <li>It is possible to ask for clarifications about the problem in case of possible ambiguity
    or error. The answers will typically be restricted to "Yes", "No" or "No comments";</li>
  <li>The scoreboard is public and updated during the contest, except for the last hour, during
    which it will be frozen.</li>
</ul>
<p>
  We follow the "1pc" rule: in a team, <b>only one person is allowed to use a computer at any
    given point in time.</b>
</p>
<ul>
  <li>For onsite participants, this means that a team will only have access to one computer
    during a contest.</li>
  <li>Online participants may use different computers (e.g. when the members of a team are not
    participating in the same room). We rely on your honesty to keep track and make sure that
    only one person is using a computer at any given time.</li>
</ul>
<p>
  Using the Internet during a contest is not forbidden. <b>However, if your team is preparing for
    ICPC or a similar contest, it is heavily discouraged; we recommend using an ICPC-compliant Team
    Reference Document instead.</b> Communicating with other people or large language models (e.g.
  talking to other people about the problems, asking questions on stackoverflow or similar forums,
  prompting ChatGPT) is forbidden.
</p>
<p>
  Terms and conditions of <a href="legal.php">the camp</a> and
  <a href="https://support.eolymp.com/policies/terms">the contest system</a> apply.
</p>

