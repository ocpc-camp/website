<h2>Detailed Information</h2>

<h4>What does the camp consist of?</h4>
<p>
  We believe that in programming competitions, the most important factor in success
  is experience, and the camp schedule reflects that. On most camp days, you will
  participate in a 5-hour training contest with your team. After the contest, you
  have the opportunity to listen to the contest analysis and discussion, presented
  by the author or one of our coaches. In addition to training your competition
  skills, this is a great chance to meet with other top teams and make new friends!
</p>

<h4>Where is the camp held?</h4>
<p>
  The camp will be held in the Department of Mathematics, J. J. Strossmayer University of Osijek.
</p>

